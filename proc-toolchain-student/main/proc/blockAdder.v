module blockAdder(result, P, G, A, B, Cin);
    input [7:0] A, B;
    input Cin;
    output [7:0] result;
    output P, G;
    wire [7:0] g, p, c;
    wire i1, i2, i3, i4, i5, i6, i7, i8, i9, i10, i11, i12, i13, i14, i15, i16, i17, i18, i19, i20, i21, i22, i23, i24, i25, i26, i27, i28, i29, i30, i31, i32, i33, i34, i35;
    functionCalc fc(g, p, A, B);
    assign c[0] = Cin;
    and (i1, p[0], Cin);
    or (c[1], g[0], i1);
    //assign c[1] = g[0] | (p[0] & Cin);
    and (i2, p[1], p[0], Cin);
    and (i3, p[1], g[0]);
    or (c[2], g[1], i3, i2);
    //assign c[2] = g[1] | (p[1] & g[0]) | (p[1] & p[0] & Cin);
    and (i4, p[2], p[1], p[0], Cin);
    and (i5, p[2], p[1], g[0]);
    and (i6, p[2], g[1]);
    or (c[3], g[2], i6, i5, i4);
    //assign c[3] = g[2] | (p[2] & g[1]) | (p[2] & p[1] & g[0]) | (p[2] & p[1] & p[0] & Cin);
    and (i7, p[3], p[2], p[1], p[0], Cin);
    and (i8, p[3], p[2], p[1], g[0]);
    and (i9, p[3], p[2], g[1]);
    and (i10, p[3], g[2]);
    or (c[4], g[3], i7, i8, i9, i10);
    //assign c[4] = g[3] | (p[3] & g[2]) | (p[3] & p[2] & g[1]) | (p[3] & p[2] & p[1] & g[0]) | (p[3] & p[2] & p[1] & p[0] & Cin);
    and (i11, p[4], p[3], p[2], p[1], p[0], Cin);
    and (i12, p[4], p[3], p[2], p[1], g[0]);
    and (i13, p[4], p[3], p[2], g[1]);
    and (i14, p[4], p[3], g[2]);
    and (i15, p[4], g[3]);
    or  (c[5], g[4], i11, i12, i13, i14, i15);
    //assign c[5] = g[4] | (p[4] & g[3]) | (p[4] & p[3] & g[2]) | (p[4] & p[3] & p[2] & g[1]) | (p[4] & p[3] & p[2] & p[1] & g[0]) | (p[4] & p[3] & p[2] & p[1] & p[0] & Cin);
    and (i16, p[5], p[4], p[3], p[2], p[1], p[0], Cin);
    and (i17, p[5], p[4], p[3], p[2], p[1], g[0]);
    and (i18, p[5], p[4], p[3], p[2], g[1]);
    and (i19, p[5], p[4], p[3], g[2]);
    and (i20, p[5], p[4], g[3]);
    and (i21, p[5], g[4]);
    or  (c[6], g[5], i16, i17, i18, i19, i20, i21);
    //assign c[6] = g[5] | (p[5] & g[4]) | (p[5] & p[4] & g[3]) | (p[5] & p[4] & p[3] & g[2]) | (p[5] & p[4] & p[3] & p[2] & g[1]) | (p[5] & p[4] & p[3] & p[2] & p[1] & g[0]) | (p[5] & p[4] & p[3] & p[2] & p[1] & p[0] & Cin);
    and (i22, p[6], p[5], p[4], p[3], p[2], p[1], p[0], Cin);
    and (i23, p[6], p[5], p[4], p[3], p[2], p[1], g[0]);
    and (i24, p[6], p[5], p[4], p[3], p[2], g[1]);
    and (i25, p[6], p[5], p[4], p[3], g[2]);
    and (i26, p[6], p[5], p[4], g[3]);
    and (i27, p[6], p[5], g[4]);
    and (i28, p[6], g[5]);
    or  (c[7], g[6], i22, i23, i24, i25, i26, i27, i28);
    //assign c[7] = g[6] | (p[6] & g[5]) | (p[6] & p[5] & g[4]) | (p[6] & p[5] & p[4] & g[3]) | (p[6] & p[5] & p[4] & p[3] & g[2]) | (p[6] & p[5] & p[4] & p[3] & p[2] & g[1]) | (p[6] & p[5] & p[4] & p[3] & p[2] & p[1] & g[0]) | (p[6] & p[5] & p[4] & p[3] & p[2] & p[1] & p[0] & Cin);
    and (i29, p[7], p[6], p[5], p[4], p[3], p[2], p[1], g[0]);
    and (i30, p[7], p[6], p[5], p[4], p[3], p[2], g[1]);
    and (i31, p[7], p[6], p[5], p[4], p[3], g[2]);
    and (i32, p[7], p[6], p[5], p[4], g[3]);
    and (i33, p[7], p[6], p[5], g[4]);
    and (i34, p[7], p[6], g[5]);
    and (i35, p[7], g[6]);
    or (G, g[7], i29, i30, i31, i32, i33, i34, i35);
    //assign G = g[7]|(p[7] & g[6]) | (p[7] & p[6] & g[5]) | (p[7] & p[6] & p[5] & g[4]) | (p[7] & p[6] & p[5] & p[4] & g[3]) | (p[7] & p[6] & p[5] & p[4] & p[3] & g[2]) | (p[7] & p[6] & p[5] & p[4] & p[3] & p[2] & g[1]) | (p[7] & p[6] & p[5] & p[4] & p[3] & p[2] & p[1] & g[0]);
    and (P, p[7], p[6], p[5], p[4], p[3], p[2], p[1], p[0]);
    xor (result[0], A[0],B[0], c[0]);
    xor (result[1], A[1],B[1], c[1]);
    xor (result[2], A[2],B[2], c[2]);
    xor (result[3], A[3],B[3], c[3]);
    xor (result[4], A[4],B[4], c[4]);
    xor (result[5], A[5],B[5], c[5]);
    xor (result[6], A[6],B[6], c[6]);
    xor (result[7], A[7],B[7], c[7]);
endmodule